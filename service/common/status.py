HTTP_415_UNSUPPORTED_MEDIA_TYPE = 415
HTTP_422_UNPROCESSABLE_ENTITY = 422
HTTP_409_CONFLICT = 409

# Server error - 5xx
HTTP_500_INTERNAL_SERVER_ERROR = 500
